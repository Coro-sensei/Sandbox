# Prog04: Create a program that ask the user to input their fullname. Print the input in all lower case.
# Example:
# Input: Juan Dela Cruz
# Output: juan dela cruz

# Input the name 
name_of_user = input("Enter name: ")

# Conversion to lower case
lower_cased = name_of_user.lower()

# Print the converted name
print("Name in lowercase: ", lower_cased)