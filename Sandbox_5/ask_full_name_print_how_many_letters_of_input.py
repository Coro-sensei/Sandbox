# Prog08: Create a program that ask the user to input their fullname. Print the number of characters in the input.
# Example:
# Input: Juan Dela Cruz
# Output: 14 

# Input name 
name_of_user = input("Enter name: ")

# Count the characters used in the name
characters = len(name_of_user)

# Print the result 
print("Number of characters in your name: ", characters)
